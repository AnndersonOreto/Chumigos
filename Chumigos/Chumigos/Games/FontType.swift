enum FontType: Substring {
    case BLACK = "Rubik-Black"
    case BLACK_ITALIC = "Rubik-BlackItalic"
    case BOLD = "Rubik-Bold"
    case BOLD_ITALIC = "Rubik-BoldItalic"
    case ITALIC = "Rubik-Italic"
    case LIGHT = "Rubik-Light"
    case LIGHT_ITALIC = "Rubik-LightItalic"
    case MEDIUM = "Rubik-Medium"
    case MEDIUM_ITALIC = "Rubik-MediumItalic"
    case REGULAR = "Rubik-Regular"
}
